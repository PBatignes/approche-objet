package entites;

public class AdressePostale {

	public int numRue;
	public String libeleRue;
	public int codePostal;
	public String ville;
	
	public AdressePostale (int numRue, String libeleRue, int codePostal, String ville) {
		
		this.numRue = numRue;
		this.libeleRue = libeleRue;
		this.codePostal = codePostal;
		this.ville = ville;
		
	}

}
